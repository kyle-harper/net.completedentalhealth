			<h2><br />Brush at the Right Times</h2>
			<p>
				We recommend brushing twice daily, once in the morning and again in the evening. More is not necessarily better, as excessive brushing can wear down the enamel surface of your teeth and damage your gums. Be sure to wait at least 30 minutes after consuming acidic foods such as coffee, orange juice, tomato sauce. Use a pH balancing mouthwash or rinse with water if you want to clean your mouth immediately after meals.
			</p>
			<h3><br />Floss Effectively</h3>
			<p>
				We recommend flossing once daily. If you are new to flossing, try starting with every other day and work up to daily. Want to make the most of your flossing? Try flossing after brushing with a pH balancing fluoride toothpaste&#151;this will help press the fluoride into the pockets surrounding your teeth and will result in healthier gums.
			</p>
			<h4><br />Choose the Right Mouthwash</h4>
			<p>
				Avoid products containing alcohol, which can cause irritation and inflammation in the gums. Even if they prevent gingivitis, they generally do very little to protect your tooth enamel. Go for something pH balanced, with xylitol such as <a href="products-and-services/prevention/cleanings-and-checkups/products.php">CariFree CTx3 Rinse</a>.
			</p>
			<h5><br />Acidic Foods are a Hidden Danger</h5>
			<p>
				We often see patients with excellent oral hygiene regimens who still experience tooth decay. One of the primary causes of this is acidic foods. Acidic foods such as coffee, orange juice, and tomato sauce temporarily soften the hard enamel surface of teeth. Failure to rinse away the acid, or worse still brushing vigorously after exposure to acidic foods, will cause the enamel to erode away. Over time, this can compromise the strucutre of the teeth and may require extensive cosmetic dental treatment to repair. Our recommendation? If you cannot avoid acidic foods (it is near impossible to do so), rinse with water or a pH balancing mouthwash immediately after your meal. Also, wait at least 30 minutes after the meal to brush your teeth.
			</p>
            <h6>Avoid Dry Mouth</h6>
			<p>
				Genetics, prescription medications, and diet can all lead to a condition known as dry mouth, where the salivary glands do not produce enough saliva to keep the mouth moist. Dry mouth can lead to increased bacterial growth in the mouth, leading to bad breath, tooth decay, and gum disease. To avoid this condition, be sure to stay hydrated by drinking plenty of water. If dry mouth persists, we recommend using a <a href="products-and-services/prevention/cleanings-and-checkups/products.php">dry mouth gel</a>.
			</p>
			<h6>Protect your Smile for the Long Haul</h6>
			<p>
				Over the long term, brushing and flossing alone are not enough to guarantee the health of your smile. We recommend <a href="products-and-services/prevention/cleanings-and-checkups/treatments.php">professional cleanings</a> at least once every six months. We also recommend annual checkups including x-rays and a comprehensive dental exam. It is also important to consider how to avoid physical damage to your teeth; we recommend wearing appropriate mouth guards during sporting activities, and <a href="products-and-services/prevention/index.php">nightguards</a> to protect teeth from damage due to clenching or grinding during sleep.
